#include <stdio.h>

#define PI 3.14159
#define MAX_SIZE 100

int main() {
    // Constants
    const int LENGTH = 10;
    const char NEWLINE = '\n';
    
    // Output
    printf("PI: %.2f\n", PI);
    printf("MAX_SIZE: %d\n", MAX_SIZE);
    printf("NEWLINE: %c", NEWLINE);
    printf("LENGTH: %d\n", LENGTH);
    
    return 0;
}
